class Solution {
  public:
    string removePair(string s) {
        string ans;
        for(auto ch:s){
            if(ans.empty()||ch!=ans.back())
                ans+=ch;
            else
                ans.pop_back();
        }
        return ans.empty()?"-1":ans;
    }
};


//{ Driver Code Starts.

int main(){
    int t;
    cin>>t;
    while(t--){
        string s; 
        cin>>s;
        
        Solution obj;
        string res = obj.removePair(s);
        
        cout<<res<<endl;
        
    }
}

