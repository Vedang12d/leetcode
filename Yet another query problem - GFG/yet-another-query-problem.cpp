class Solution {
  public:
    vector<int> solveQueries(int N, int num, vector<int> &A, vector<vector<int>> &Q) {
        vector<int> freq(N);
        unordered_map<int,int> mp;
        for(int i=N-1;i>=0;i--)
            freq[i]=++mp[A[i]];
        vector<int> ans;
        for(auto &q:Q){
            int ct=0;
            for(int i=q[0];i<=q[1];i++){
                if(freq[i]==q[2])
                    ct++;
            }
            ans.push_back(ct);
        }
        return ans;
    }
};


